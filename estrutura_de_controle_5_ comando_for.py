# teste de estrutura de controle comando "for."

# o looping será executado 5x.
for i in range(5):
    print('Contando ' + str(i))

# o looping irá contar de 10 a 15.
for i in range(10, 15):
    print('Contando ' + str(i))

# o looping irá contar do 0 ao 20, pulando de 2 em 2.
for i in range(0, 20, 2):
    print('Contando ' + str(i))

# o lopping fará uma contagem reversa de 1 em 1.
for i in range(10, -1, -1):
    print('Contando ' + str(i))

# o lopping do vai do 10 ao 15, contando a casa inicial (10) até (13) e termina a por conta da condição de parada.
for i in range(10, 15, 3):
    print('Contando ' + str(i))
